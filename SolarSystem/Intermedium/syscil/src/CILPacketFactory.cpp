namespace syscil {
CILPacketFactoryImpl* CILPacketFactory::ms_pImpl = nullptr;

void CILPacketFactory::SetImplementation(CILPacketFactoryImpl *ptr) {
	ms_pImpl = ptr;
}

CILPacket* CILPacketFactory::Create(PCOL_TYPE type) {
	if (ms_pImpl != nullptr) {
		return ms_pImpl->Create(type);
	}
	return nullptr;
}
}
